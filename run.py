def check_module(module_name, package_name=None):
    """–ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞–ª–∏—á–∏—è –º–æ–¥—É–ª—è"""
    try:
        spec = importlib.util.find_spec(module_name)
        if spec is not None:
            print(f"‚úÖ {module_name} —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω")
            return True
        else:
            print(f"‚ùå {module_name} –Ω–µ –Ω–∞–π–¥–µ–Ω")
            if package_name:
                print(f"   –£—Å—Ç–∞–Ω–æ–≤–∏—Ç–µ: pip install {package_name}")
            return False
    except ImportError:
        print(f"‚ùå {module_name} –Ω–µ –Ω–∞–π–¥–µ–Ω")
        if package_name:
            print(f"   –£—Å—Ç–∞–Ω–æ–≤–∏—Ç–µ: pip install {package_name}")
        return False

def check_dependencies():
    """–ü—Ä–æ–≤–µ—Ä–∫–∞ –≤—Å–µ—Ö –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–µ–π"""
    print("üîç –ü—Ä–æ–≤–µ—Ä–∫–∞ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–µ–π...")
    
    dependencies = [
        ("PyQt5", "PyQt5"),
        ("requests", "requests"),
        ("PIL", "Pillow"),
        ("cryptography", "cryptography"),
    ]
    
    missing_deps = []
    
    for module, package in dependencies:
        if not check_module(module, package):
            missing_deps.append(package)
    
    if missing_deps:
        print(f"\n‚ùå –û—Ç—Å—É—Ç—Å—Ç–≤—É—é—Ç –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–∏: {', '.join(missing_deps)}")
        print("üì¶ –£—Å—Ç–∞–Ω–æ–≤–∏—Ç–µ –∏—Ö –∫–æ–º–∞–Ω–¥–æ–π:")
        print(f"   pip install {' '.join(missing_deps)}")
        print("\n–ò–ª–∏ —É—Å—Ç–∞–Ω–æ–≤–∏—Ç–µ –≤—Å–µ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–∏:")
        print("   pip install -r requirements.txt")
        return False
    
    print("\n‚úÖ –í—Å–µ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–∏ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω—ã!")
    return True

def install_dependencies():
    """–ê–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∞—è —É—Å—Ç–∞–Ω–æ–≤–∫–∞ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–µ–π"""
